function mostrarFinalidad(){
    document.getElementById('finalidad').style.display = 'block';
    document.getElementById('servicios').style.display = 'none';
    document.getElementById('apoyanos').style.display = 'none';
}



function mostrarServicios(){
    document.getElementById('finalidad').style.display = 'none';
    document.getElementById('servicios').style.display = 'block';
    document.getElementById('apoyanos').style.display = 'none';
}



function mostrarApoyanos(){
    document.getElementById('finalidad').style.display = 'none';
    document.getElementById('servicios').style.display = 'none';
    document.getElementById('apoyanos').style.display = 'block';
}